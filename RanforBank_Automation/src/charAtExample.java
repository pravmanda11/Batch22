
public class charAtExample {

	public static void main(String[] args) {

		String Name = "Praveen123";

		System.out.println(Name.charAt(0)); // P
		System.out.println(Name.charAt(9)); // 3
		

		char ps1 = Name.charAt(0); // P

		System.out.println(ps1);

		char ps9 = Name.charAt(9); // P

		System.out.println(ps9);

	}

}
